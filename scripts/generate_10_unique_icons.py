def build(name, data) -> Image.Image:
    img = Image.new("RGB", (GRID, GRID))
    bg_top = data["bg_top"]
    bg_bot = data["bg_bot"]
    colors = data["colors"]
    scale = 3
    
    for y in range(GRID):
        t = y / (GRID - 1)
        r = round(bg_top[0] + (bg_bot[0] - bg_top[0]) * t)
        g = round(bg_top[1] + (bg_bot[1] - bg_top[1]) * t)
        b = round(bg_top[2] + (bg_bot[2] - bg_top[2]) * t)
        for x in range(GRID):
            img.putpixel((x, y), (r, g, b))
            
    draw = ImageDraw.Draw(img)
    
    sprite = data["sprite"]
    off_x = (GRID - 16 * scale) // 2
    off_y = (GRID - 16 * scale) // 2 + 2
    
    for r, row in enumerate(sprite):
        for c, char in enumerate(row):
            if char in colors:
                color = colors[char]
                x0 = off_x + c * scale
                y0 = off_y + r * scale
                x1 = x0 + scale - 1
                y1 = y0 + scale - 1
                draw.rectangle([x0, y0, x1, y1], fill=color)

    # Add a border to the whole grid to make it look nicer when viewing on desktop
    draw.rectangle([0, 0, GRID-1, GRID-1], outline=(0, 0, 0, 100))

    return img.resize((OUT, OUT), Image.NEAREST)

def main():
    desktop_dir = DESKTOP / "FitnessStreaks_Unique_Icons"
    desktop_dir.mkdir(parents=True, exist_ok=True)
    
    for name, data in SPRITES.items():
        icon = build(name, data)
        path = desktop_dir / f"{name}.png"
        icon.save(path, format="PNG")
        print(f"Generated {path}")

if __name__ == "__main__":
    main()
